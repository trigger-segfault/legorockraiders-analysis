Asm_MulFIX:
	mult	a0,a1
	mflo	v0
	mfhi	v1
	nop
	nop
	srl	v0,v0,12		; lose unwanted data
	sll	v1,v1,20
	jr	ra
	or	v0,v0,v1


Asm_DivFIX:
	bne	a1,zero,@ok
	move	t0,zero
	jr	ra
	move	v0,zero
@ok	bgez	a0,@ok1
	li	v1,13
	li	t0,-1
	sub	a0,zero,a0
@ok1	move	t1,zero
	bgez	a1,@loop
	move	v0,zero
	li	t1,-1
	sub	a1,zero,a1
	beq	a1,zero,@out
	nop

@loop	divu	a0,a1
	mflo	t2			;Get result of divide
	mfhi	a0			;Get remainder
	sll	v0,v0,1
	sll	a0,a0,1
	add	v1,v1,-1
	bne	v1,zero,@loop
	add	v0,v0,t2

@out	beq	t0,zero,@posa0
	nop
	sub	v0,zero,v0
@posa0	beq	t1,zero,@posa1
	nop
	sub	v0,zero,v0
@posa1	jr	ra
	nop

